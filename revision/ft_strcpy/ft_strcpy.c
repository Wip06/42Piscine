#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;

	while (src[i] != dest[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char *src;
	char dest[5];

	src = "Hello";
	printf("%s, %s", src, ft_strcpy(dest, src));
}
