#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	char	l;

	i = 0;
	if (argc == 2)
	{
		while(argv[1][i] != '\0')
		{
			l = argv[1][i];
			if (argv[1][i] >= 'A' && argv[1][i] <= 'Z')
				l += 32;
			if (argv[1][i] >= 'a' && argv[1][i] <= 'z')
				l -= 32;
			write(1, &l, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
